const mongoose = require('mongoose')

const userAuthAdminSchema = new mongoose.Schema({
    email: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
})

module.exports = mongoose.model('user_auth_admin', userAuthAdminSchema)